COLOURS = {
    "selenium":      {"header": "1A6B3C", "alt": "D6F0E0", "title": "0D4A2B"},
    "appium":        {"header": "1A3A6B", "alt": "D6E4F7", "title": "0B2545"},
    "load_testing":  {"header": "6B1A1A", "alt": "F7D6D6", "title": "4A0B0B"},
    "validation":    {"header": "5C1A6B", "alt": "EDD6F7", "title": "3A0B4A"},
}

thin = Side(style="thin", color="CCCCCC")
BORDER = Border(left=thin, right=thin, top=thin, bottom=thin)

HEADERS = [
    "TC ID", "Module", "Sub-Module", "Test Case Title",
    "Pre-conditions", "Test Steps", "Expected Result",
    "Priority", "Status", "Remarks"
]

COL_WIDTHS = [10, 20, 22, 45, 35, 60, 45, 10, 12, 20]


def style_header_row(ws, colour_key):
    c = COLOURS[colour_key]
    hdr_fill  = PatternFill("solid", fgColor=c["header"])
    hdr_font  = Font(bold=True, color="FFFFFF", size=11, name="Calibri")
    hdr_align = Alignment(horizontal="center", vertical="center", wrap_text=True)

    for col_idx, (hdr, width) in enumerate(zip(HEADERS, COL_WIDTHS), start=1):
        cell = ws.cell(row=1, column=col_idx, value=hdr)
        cell.fill      = hdr_fill
        cell.font      = hdr_font
        cell.alignment = hdr_align
        cell.border    = BORDER
        ws.column_dimensions[get_column_letter(col_idx)].width = width

    ws.row_dimensions[1].height = 30
    ws.freeze_panes = "A2"


def write_row(ws, row_num, data, colour_key):
    c = COLOURS[colour_key]
    is_alt = (row_num % 2 == 0)
    fill   = PatternFill("solid", fgColor=c["alt"]) if is_alt else PatternFill("solid", fgColor="FFFFFF")
    font   = Font(size=10, name="Calibri")
    align  = Alignment(vertical="top", wrap_text=True)

    for col_idx, value in enumerate(data, start=1):
        cell            = ws.cell(row=row_num, column=col_idx, value=value)
        cell.fill       = fill
        cell.font       = font
        cell.alignment  = align
        cell.border     = BORDER

    ws.row_dimensions[row_num].height = 45


